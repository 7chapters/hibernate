package com.tutorial.hibernate.algorithms.uuid_5.oracle;

public class Bank
{
	
	String acno, name;
	

	public void setAcno(String acno)
	{
		this.acno = acno;
	}

	public String getAcno()
	{
		return acno;
	}


	public void setName(String name)
	{
		this.name = name;
	}

	public String getName()
	{
		return name;
	}

}
